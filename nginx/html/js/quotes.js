  function createQuoteElement(quote) {
    const div = document.createElement('div');
    div.className = 'quote-box';
    div.textContent = quote;
    return div;
  }
  
  function showRandomQuote() {
    if (typeof overlayVisible !== 'undefined' && !overlayVisible) return;
    const overlay = document.getElementById('loadingOverlay');
    const quoteEl = createQuoteElement(
      philosophicalQuotes[Math.floor(Math.random() * philosophicalQuotes.length)]
    );
    overlay.appendChild(quoteEl);
  
    const boxWidth = quoteEl.offsetWidth;
    const boxHeight = quoteEl.offsetHeight;
    const overlayWidth = overlay.offsetWidth;
    const overlayHeight = overlay.offsetHeight;
  
    let x = 0, y = 0;
    let inCenterZone = true;
    do {
      x = Math.random() * (overlayWidth - boxWidth);
      y = Math.random() * (overlayHeight - boxHeight);
      const centerX = x + boxWidth / 2;
      const centerY = y + boxHeight / 2;
      inCenterZone = (centerX > 0.4 * overlayWidth  && centerX < 0.6 * overlayWidth) &&
                     (centerY > 0.4 * overlayHeight && centerY < 0.6 * overlayHeight);
    } while (inCenterZone);
  
    quoteEl.style.left = x + 'px';
    quoteEl.style.top  = y + 'px';
    quoteEl.style.visibility = 'visible';
  
    setTimeout(() => {
      quoteEl.style.animation = 'fadeOut 1s forwards';
      setTimeout(() => {
        if (quoteEl.parentNode) {
          quoteEl.parentNode.removeChild(quoteEl);
        }
      }, 1000);
    }, 8000);
  }
  
  function startQuotes() {
    setTimeout(() => {
      showRandomQuote();
      quoteTimer = setInterval(showRandomQuote, 10000);
    }, 1000);
  }
  